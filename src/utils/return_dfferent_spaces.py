def get_hashes(count: int):
    return "#" * count


def get_spaces(count: int):
    return " " * count


def get_newlines(count: int):
    return "\n" * count

def get_tabs(count: int):
    return "\t" * count


def get_newlines_and_spaces(count: int):
    return "\n" * count + " " * count


def get_newlines_and_tabs(count: int):
    return "\n" * count + "\t" * count

